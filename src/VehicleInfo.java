public class VehicleInfo {

    int VIN;
    double odometer;
    double gasConsumed;
    double odoLastOilChg;
    double engSizeL;


    // ***** empty constructor *****
    public VehicleInfo() {
    }

    // ***** getters and setters *****
    public int getVIN() {
        return VIN;
    }

    public void setVIN(int VIN) {
        this.VIN = VIN;
    }

    public double getOdometer() {
        return odometer;
    }

    public void setOdometer(double odometer) {
        this.odometer = odometer;
    }

    public double getGasConsumed() {
        return gasConsumed;
    }

    public void setGasConsumed(double gasConsumed) {
        this.gasConsumed = gasConsumed;
    }

    public double getOdoLastOilChg() {
        return odoLastOilChg;
    }

    public void setOdoLastOilChg(double odoLastOilChg) {
        this.odoLastOilChg = odoLastOilChg;
    }

    public double getEngSizeL() {
        return engSizeL;
    }

    public void setEngSizeL(double engSizeL) {
        this.engSizeL = engSizeL;
    }

}


